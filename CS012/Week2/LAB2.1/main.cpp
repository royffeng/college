int fileSum(const string &filename){
    ifstream inFS;
    int sum;
    int tempAdd;
    
    inFS.open(filename);
    
    if(!inFS.is_open()){
        cout << "Error opening "<< filename << endl;
        exit(1);
    }
    
    while(inFS >> tempAdd){
        sum += tempAdd;
    }
    
    inFS.close();
    
    return sum;
}
